import java.util.*;
public class Shorthand {
   public static void main(String args[]){
    int a=5;
    System.out.println(a);
    Scanner sc=new Scanner(System.in);
    int x1=sc.nextInt();
    a=a+ x1;
    System.out.println(a);

    int x2=sc.nextInt();
    a=a+x2;
    System.out.println(a);


   } 
}
